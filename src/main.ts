import { NestFactory } from '@nestjs/core';
import { ValidationPipe, Logger } from '@nestjs/common';
import { SwaggerModule, DocumentBuilder } from '@nestjs/swagger';
import { ConfigService } from '@nestjs/config';
import helmet from 'helmet';
import * as compression from 'compression';

import { AppModule } from './app.module';
import { HttpExceptionFilter } from './common/filters/http-exception.filter';
import { LoggingInterceptor } from './common/interceptors/logging.interceptor';

async function bootstrap() {
  const app = await NestFactory.create(AppModule);
  const configService = app.get(ConfigService);
  const logger = new Logger('Bootstrap');

  // Security middleware
  app.use(helmet());
  app.use(compression());

  // Global pipes
  app.useGlobalPipes(
    new ValidationPipe({
      whitelist: true,
      forbidNonWhitelisted: true,
      transform: true,
      transformOptions: {
        enableImplicitConversion: true,
      },
    }),
  );

  // Global filters and interceptors
  app.useGlobalFilters(new HttpExceptionFilter());
  app.useGlobalInterceptors(new LoggingInterceptor());

  // CORS configuration
  const allowedOrigins = process.env.NODE_ENV === 'production' 
    ? [
        'https://smartanalyzer.vercel.app',
        'https://smart-document-analyzer.vercel.app',
        ...(process.env.ALLOWED_ORIGINS?.split(',') || [])
      ]
    : true;

  app.enableCors({
    origin: allowedOrigins,
    credentials: true,
    methods: ['GET', 'POST', 'PUT', 'DELETE', 'PATCH', 'OPTIONS'],
    allowedHeaders: ['Content-Type', 'Authorization', 'X-Requested-With'],
    optionsSuccessStatus: 200,
  });

  // Swagger documentation
  const config = new DocumentBuilder()
    .setTitle('Smart Document Analyzer API')
    .setDescription('API para anÃ¡lise inteligente de documentos acadÃªmicos usando IA')
    .setVersion('1.0')
    .addBearerAuth()
    .addTag('auth', 'AutenticaÃ§Ã£o e autorizaÃ§Ã£o')
    .addTag('users', 'Gerenciamento de usuÃ¡rios')
    .addTag('documents', 'Upload e gerenciamento de documentos')
    .addTag('analysis', 'AnÃ¡lise de documentos com IA')
    .build();

  const document = SwaggerModule.createDocument(app, config);
  SwaggerModule.setup('api/docs', app, document, {
    swaggerOptions: {
      persistAuthorization: true,
    },
  });

  const port = configService.get('PORT', 3333);
  await app.listen(port);

  logger.log(`ðŸš€ Application is running on: http://localhost:${port}`);
  logger.log(`ðŸ“š Swagger documentation: http://localhost:${port}/api/docs`);
}

bootstrap();
